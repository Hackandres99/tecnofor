Un vector es un segmento orientado. No es más que un trazo de recta, en el que se diferencia claramente su origen y su extremo. <br><br>

<label class="strong">Componentes de un vector.</label><br><br>

Son dos valores que vienen dados en forma de par de números, los cuáles indican las unidades que tenemos que desplazarnos horizontalmente y verticalmente respectivamente, para llegar desde el origen del vector al extremo de éste. <br><br>

Es decir si el vector <label class="strong">v</label> = (3,2), esto nos indica que debemos desplazarnos tres unidades a la derecha y dos unidades hacia arriba para llegar del origen al extremo del vector. <br><br>

<label class="strong">Suma y resta de vectores en el plano</label>

<ul class="list">
    <li>Para sumar dos vectores se suman sus coordenadas.</li>
    <li>Para restar dos vectores se restan sus coordenadas.</li>
</ul>

