La potencia de exponente natural de un número entero positivo, es igual a multiplicar dicho número por sí mismo tantas veces como indique el exponente, y su signo depende del signo de la base. <br><br>
Si la base es positiva el resultado es positivo. <br><br>
<label class="mark">Ejemplo:</label>  

<ol class="list">
    <li>5² = 25</li>
    <li>3<sup>3</sup> =  27</li>
</ol>

Si la base es negativa el resultado 

<ul class="list">
    <li>Positivo  +, si el exponente es par.</li>
    <li>Negativo  -, si el exponente es impar.</li>
</ul>

De manera general podemos decir que la potencia de exponente natural de un número entero es otro número entero, cuyo valor absoluto es el valor absoluto de la potencia y cuyo signo es el que se deduce de la aplicación de las siguientes reglas presentadas:<br><br>
Las potencias de exponente par son siempre positivas.

<ul class="list">
    <li>+ par = +</li>
    <li>- par = +</li>
</ul>

Las potencias de exponente impar tienen el mismo signo de la base.

<ul class="list">
    <li>+ impar = +</li>
    <li>- impar = -</li>
</ul>