Producto Sean a, b, n, m, t ∈ R. Utilizando las propiedades del producto de números reales, así como la potenciación de números reales con exponentes naturales, el producto de los monomios at<sup>n</sup> y bt<sup>m</sup> nos da el resultado siguiente: <br><br>

(at<sup>n</sup> )( bt<sup>m</sup> ) = abt<sup>n</sup>t<sup>m</sup> = abt<sup>n+m</sup> <br><br>

Este tipo de operaciones se puede llevar a cabo de dos formas distintas.

<ul class="list">
    <li>P(x) = 2x² - 3</li>
    <li>Q(x) = 2x³ - 3x² + 4x</li>
</ul>

<label class="mark">Primera opción</label>

<ol class="list">
    <li>Se multiplica cada monomio del primer polinomio por todos los elementos del segundo polinomio.</li>
        P(x) · Q(x) = (2x² - 3) · (2x³ - 3x² + 4x)  = 4x5 - 6x4 + 8x³ - 6x³ + 9x² - 12x
    <li>Se suman los monomios del mismo grado (suma de términos semejantes) y obtenemos:</li>
        4x5 - 6x4 + 2x³ + 9x² - 12x
    <li>El polinomio obtenido es otro polinomio cuyo grado es la suma de los grados de los polinomios que se multiplicaron.</li>
        Grado del polinomio resultante = Grado de P(x) + Grado de Q(x) = 2 + 3 = 5
</ol>

<label class="mark">Segunda opción</label> <br><br>

También podemos sumar polinomios escribiendo un polinomio debajo del otro.

<ol class="list">
    <li>En cada fila se multiplica cada uno de los monomios del segundo polinomio por todos los monomios del primer polinomio.</li>
    <li>Se colocan los monomios semejantes en la misma columna y posteriormente se suman los monomios semejantes.</li>
    <li>Como la multiplicación de polinomios cumple la propiedad conmutativa, hemos tomado como polinomio multiplicador el polinomio más sencillo.</li>
</ol>

