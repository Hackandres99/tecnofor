<label class="mark">El teorema del resto dice:</label> <br><br>

Si dividimos un polinomio P(x) entre el binomio (x-a), el resto de la división es igual al valor numérico del polinomio P(a). <br><br>

<label class="strong">¿Para qué nos sirve esto?</label><br><br>

Con el teorema del resto podemos calcular el resto de una división sin tener que hacerla, siempre que dividamos un polinomio por un binomio de la forma x-a. <br><br>

Es decir: <br><br>

Si queremos saber el resto de la división P(x): Q(x) siendo: <br><br>

P(x)= 2x2+3x-2 <br>
Q(x)= x-2

