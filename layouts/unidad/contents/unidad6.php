Para dividir el polinomio P(x) entre el polinomio Q(x), necesitamos que el grado de P(x) sea mayor o igual que el grado de Q(x). <br><br>

El polinomio P(x) es el dividendo y Q(x) es el divisor. <br><br>

Escribimos el dividiendo y el divisor como en una división de números:

<ul class="list" style="list-style: none;">
    <li>P(x) <label class="division">Q(x)</label></li>
    <li>R(x) C(x)</li>
</ul>

El polinomio R(x) es el resto y C(x) es el cociente. <br><br>

El grado de R(x) es menor que el de Q(x) y el grado de C(x) es el grado de P(x) menos el de Q(x). <br><br>

