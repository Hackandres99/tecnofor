La división de polinomios se encuentra entre una gran cantidad de operaciones algebraicas, y entre ellas podemos encontrarnos con la operación especifica de dividir un polinomio entre un binomio. <br><br>

En este caso específico, cuando un polinomio se divide entre un binomio, por lo general se presenta un residuo. <br><br>

En el teorema del residuo se establece que cuando un <label class="strong">polinomio de x, f(x)</label> se divide entre <label class="strong">(x - a)</label>, en donde cualquier número a equivaler a cualquier número real o complejo, entones el residuo es <label class="strong">f(a)</label>. <br><br>

Esto quiere decir que para poder encontrar el residuo cuando el polinomio se divide entre un binomio el valor de x es igual al valor de a, o <label class="strong">f(x) = f(a)</label>. <br><br>

Así, se tiene que calcular el valor de x, el cual será igual que el valor de a. <br><br>

Si consideramos el polinomio <label class="strong">x² - 8 x + 6</label>, el cual se puede identificar como una función <label class="strong">polinomial f(x) = x² -8 x + 6</label>. <br><br>

Aquí se tiene que dividir el polinomio entre el <label class="strong">binomio x - 2</label> para poder encontrar el residuo. <br><br>

En este caso la división se puede realizar de la siguiente forma: <br><br>

<label class="mark">Uso del teorema del residuo:</label> <br><br>

f ( x ) = x² - 8 x + 6 dividido entre x - 2. <br><br>
En este caso, para poder calcular el valor de x, hacemos x - 2  = 0 <br><br>
x = 2 <br><br>

