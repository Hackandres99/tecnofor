Un sistema lineal de ecuaciones, o sistema de ecuaciones lineales o, incluso, por brevedad, sistema de ecuaciones, es un conjunto de ecuaciones de primer grado con varias incógnitas. Una solución de un sistema lineal de ecuaciones es un conjunto de números que, al sustituir cada uno una de las incógnitas, convierte todas las ecuaciones en igualdades numéricas correctas. <br><br>

Un método sencillo para resolver un sistema de dos ecuaciones con dos incógnitas, es: <br><br>

<label class="strong">Método de reducción.</label><br><br>

Este método consiste en multiplicar convenientemente ambas ecuaciones de forma que, una vez restadas, desaparezca una de las incógnitas y se pueda resolver la ecuación resultante. Una vez resuelta esta última ecuación, puede sustituirse el valor de la incógnita en una de las ecuaciones iniciales y resolver la ecuación resultante para encontrar el otro valor. <br><br>
